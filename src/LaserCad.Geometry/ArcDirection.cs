namespace LaserCad.Geometry;

/// <summary>
/// Kierunek przejscia po luku od kata poczatkowego do koncowego.
/// </summary>
public enum ArcDirection
{
    /// <summary>
    /// Kierunek przeciwny do ruchu wskazowek zegara.
    /// </summary>
    Counterclockwise = 0,

    /// <summary>
    /// Kierunek zgodny z ruchem wskazowek zegara.
    /// </summary>
    Clockwise = 1,
}
